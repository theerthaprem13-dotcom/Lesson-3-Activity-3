#Arithmetic operators(+,-,*,/,%,**(power eg.2**3),//(floor division, rounded of value))

a1=10
b1=3
print(a1+b1)
print(a1-b1)
print(a1*b1)
print(a1/b1)
print(a1**b1)
print(a1%b1)
print(a1//b1)

#Comparison operators: compares two values and return either true or false (==(equal to),!=(Not equal to),>,<,>=,<=)

a3=10
b3=5
print(a3==b3)
print(a3!=b3)
print(a3>b3)
print(a3<b3)
print(a3>=b3)
print(a3<=b3)

